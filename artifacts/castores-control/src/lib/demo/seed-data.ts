const now = new Date();
function daysAgo(n: number): Date {
  const d = new Date(now);
  d.setDate(d.getDate() - n);
  return d;
}
function daysAhead(n: number): Date {
  const d = new Date(now);
  d.setDate(d.getDate() + n);
  return d;
}
function iso(d: Date): string {
  return d.toISOString();
}

