export function buildDashboardSummary() {
  const activeProjects = DEMO_PROJECTS.filter((p) => p.status === "active");
  const completedProjects = DEMO_PROJECTS.filter((p) => p.status === "completed");
  return {
    activeProjects: activeProjects.length,
    completedProjects: completedProjects.length,
    totalProjects: DEMO_PROJECTS.length,
    totalWorkers: DEMO_USERS.filter((u) => u.role === "worker").length + 78,
    totalSupervisors: DEMO_USERS.filter((u) => u.role === "supervisor").length,
    totalClients: DEMO_USERS.filter((u) => u.role === "client").length,
    pendingMaterialRequests: DEMO_MATERIALS.filter((m) => m.status === "pending").length,
    unreadNotifications: DEMO_NOTIFICATIONS.filter((n) => !n.isRead).length,
    totalLogsToday: DEMO_LOGS.filter((l) => {
      const d = new Date(l.createdAt);
      return d.toDateString() === new Date().toDateString();
    }).length,
    totalBudget: DEMO_PROJECTS.reduce((s, p) => s + p.budget, 0),
    totalSpent: DEMO_PROJECTS.reduce((s, p) => s + p.spentAmount, 0),
  };
}
